#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned	char	*p;
	size_t				i;

	i = 0;
	p = s;
	while (i < n)
	{
		*p = '\0';
		p++;
		i++;
	}
	return (s);
}
